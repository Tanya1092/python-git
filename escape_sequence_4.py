a = "Tanya is a good girl \n she studies hard."
print(a)

b = "it's a \"sunny day\""
print(b)

c = "This is a backslash: \\"
print(c)

d = "first line \t second column"
print(d)

e = "This is the vertical tab \v see the difference"
print(e)

f = "This is the form feed \f see the ifference"
print(f)

g = "This is a carriage return \r see the difference"
print(g)

h = "This is a unicode character: \u00A9"
print(h)

i = "I like apple \\ mango"
print(i)

j = "This is a raw string: r\"C:\\newfolder\\test\""
print(j)

k = "hello\bworld"
print(k)

l = "This is a octal value: \110\145\154\154\157"
print(l)

m = "This is a hexadecimal value: \x48\x65\x6c\x6c\x6f"
print(m)
print("End of escape sequences")